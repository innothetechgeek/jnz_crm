<script>
	
	// Steps section
// Requires jQuery to work

// Go to the next step (tab switching)

  $('.new-next-step > a').click(function(event) {
    event.preventDefault();
    var href = $(this).attr('href');

    if (href.charAt(0) == '#') {     
      $('.steps-nav > li.active').removeClass('active');
      $('.steps-nav > .' + href.substring(1, href.length)).addClass('active');
      $('.steps-content.active').removeClass('active');
      $(href).addClass('active');
    } else {
      window.location.href = href;
    }
  });

// Dynamic tabs

$('.steps-nav > li > a').click(function(event) {
    event.preventDefault();
    var previous_active_tab_selector = $('.steps-nav > li.active > a').attr('href');
    var previous_active_nav = $('.steps-nav > li.active');
    var target_tab_selector = $(this).attr('href');
    previous_active_nav.removeClass('active');
    $(this).parents('li').addClass('active');
    $(previous_active_tab_selector).removeClass('active');
    $(target_tab_selector).addClass('active');
  });
</script>
